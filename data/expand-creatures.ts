function main() {
  // 1. 读取现有 creatures_verified.json
  const existing: Creature[] = JSON.parse(readFileSync(creaturesPath, 'utf8'))
  const existingIds = new Set(existing.map((c) => c.id))

  // 2. 过滤掉 id 已存在的（幂等）
  const toAdd = newCreatures.filter((c) => !existingIds.has(c.id))
  if (toAdd.length === 0) {
    console.log('没有新神兽需要添加（所有 id 已存在）。')
  } else {
    console.log(`准备添加 ${toAdd.length} 个新神兽。`)
  }

  // 3. 校验新神兽关键字段
  const idRegex = /^[a-z0-9-]+$/
  const validConfidences = new Set(['high', 'medium', 'creative'])
  const seenIds = new Set<string>()
  for (const c of toAdd) {
    if (!idRegex.test(c.id)) {
      throw new Error(`非法 id: ${c.id}`)
    }
    if (seenIds.has(c.id)) {
      throw new Error(`重复 id: ${c.id}`)
    }
    seenIds.add(c.id)
    if (!validConfidences.has(c.confidence)) {
      throw new Error(`非法 confidence: ${c.confidence} (${c.id})`)
    }
    if (!c.original_text || c.original_text.length <= 5) {
      throw new Error(`original_text 过短: ${c.id}`)
    }
    if (!c.translation || c.translation.length <= 5) {
      throw new Error(`translation 过短: ${c.id}`)
    }
    if (!c.art_description || c.art_description.length < 20) {
      throw new Error(`art_description 不足 20 字符: ${c.id} -> "${c.art_description}"`)
    }
    if (!c.scroll || c.scroll.length === 0) {
      throw new Error(`scroll 为空: ${c.id}`)
    }
    if (!c.province || c.province.length === 0) {
      throw new Error(`province 为空: ${c.id}`)
    }
  }

  // 4. 合并并写回 creatures_verified.json
  const merged = [...existing, ...toAdd]
  writeFileSync(creaturesPath, JSON.stringify(merged, null, 2) + '\n', 'utf8')
  console.log(`creatures_verified.json 已更新：${existing.length} -> ${merged.length} 条`)

  // 5. 重新生成 province_assignments.json（覆盖全部 34 省）
  const assignments: Record<string, string[]> = {}
  for (const p of ALL_PROVINCES) {
    assignments[p] = []
  }
  for (const c of merged) {
    if (c.province === '两广') {
      // 「两广」覆盖 广东 与 广西
      if (assignments['广东']) assignments['广东'].push(c.id)
      if (assignments['广西']) assignments['广西'].push(c.id)
    } else if (assignments[c.province]) {
      assignments[c.province].push(c.id)
    }
  }

  // 校验：34 省全部覆盖
  const missing = ALL_PROVINCES.filter((p) => assignments[p].length === 0)
  if (missing.length > 0) {
    throw new Error(`以下省份未覆盖: ${missing.join(', ')}`)
  }

  writeFileSync(assignmentsPath, JSON.stringify(assignments, null, 2) + '\n', 'utf8')
  console.log(`province_assignments.json 已重新生成，覆盖 ${ALL_PROVINCES.length} 个省份。`)

  // 6. 汇总输出
  console.log('\n===== 各省神兽数量 =====')
  const summary = ALL_PROVINCES.map((p) => `${p}(${assignments[p].length})`).join(' ')
  console.log(summary)
  console.log(`\n总数：${merged.length} 个神兽，覆盖 ${ALL_PROVINCES.length} 个省份。`)
}

main()
